<h2>Introduction</h2>
<p>Over the past year or so, I have dedicated over 100 hours to conceptualizing, designing, and developing my personal portfolio website. This site serves as a comprehensive showcase of my skills, projects, and experiences as a developer.</p>

<h2>Technologies Used</h2>
<p>To build a modern, responsive, and performant website, I utilized the following technologies:</p>
<ul>
    <li><strong>Svelte:</strong> A modern JavaScript framework for building user interfaces with a focus on reactivity and performance.</li>
    <li><strong>SvelteKit:</strong> The official application framework for Svelte, providing routing, server-side rendering, and other essential features.</li>
    <li><strong>Tailwind CSS:</strong> A utility-first CSS framework that allows for rapid UI development with highly customizable design.</li>
    <li><strong>TypeScript:</strong> A statically typed superset of JavaScript that enhances code quality through type safety and tooling support.</li>
    <li><strong>GitHub Pages:</strong> A free hosting service provided by GitHub for deploying static websites directly from a repository.</li>
</ul>

<h2>Development Process</h2>
<p>The development of the portfolio site involved several key stages:</p>

<h3>Planning and Design</h3>
<p>The initial phase was focused on planning the site structure and design. This included sketching layouts, choosing a color palette, and deciding on the content hierarchy to ensure a user-friendly experience.</p>

<h3>Setting Up the Development Environment</h3>
<p>I set up the project using SvelteKit, integrating TypeScript for type safety and Tailwind CSS for styling. This combination allowed for efficient development with real-time feedback and rapid prototyping.</p>

<h3>Building Components</h3>
<p>The site is composed of reusable Svelte components, such as navigation bars, footers, project cards, and skill icons. This modular approach enhances maintainability and scalability.</p>

<h3>Implementing Responsive Design</h3>
<p>Using Tailwind CSS, I ensured the site is fully responsive across various devices and screen sizes. Media queries and responsive utilities were extensively used to achieve this adaptability.</p>

<h3>Content Integration</h3>
<p>I incorporated personal content, including an overview of my skills, detailed descriptions of projects, and links to social profiles. Dynamic routing in SvelteKit was utilized to create individual pages for each project.</p>

<h3>Deployment</h3>
<p>The final step was deploying the site to GitHub Pages. The build process generates static assets optimized for performance, which are then hosted directly from the repository.</p>

<h2>Challenges Faced</h2>
<p>Throughout the development process, I encountered and overcame several challenges:</p>
<ul>
    <li><strong>Dynamic Routing with SvelteKit:</strong> Setting up dynamic routes for project pages required a deep understanding of SvelteKit's file-based routing system.</li>
    <li><strong>TypeScript Configuration:</strong> Integrating TypeScript involved configuring type definitions and ensuring compatibility with Svelte components.</li>
    <li><strong>State Management:</strong> Managing global state across components was addressed using Svelte's built-in stores.</li>
    <li><strong>Deployment Issues:</strong> Configuring GitHub Pages for a SvelteKit project required custom settings to handle client-side routing correctly.</li>
</ul>

